const person = {
    firstName: 'Moe',
    lastName: 'Majed',

    set fullName(name) {
        // lets throw an exception error
        if (typeof name !== 'string') //if type of name not string 
            throw new Error('Type of name is not String'); //throw indicates that error is exception now

        const parts = name.split(' ')
        if (parts.length !== 2) //not 2 array elements (Fname, Lname).
            throw new Error('Enter first and last Name') //throw is like return.

        this.firstName = parts[0];
        this.lastName = parts[1];
        return `${this.firstName} ${this.lastName}`
    },
}

// Try&Catch (also automatically generated by JS)
try { //try this code (could have exception).
    person.fullName = 'aa aa'; //not string is an error. THE name. 
}
catch (e) { //(e : is our exception).| if an exception found then:
    alert(e)
}

